<?php
namespace JaguarJack\Generate\Build;


use PhpParser\Comment\Doc;
use PhpParser\Node\Expr\PropertyFetch;
use PhpParser\Node\Identifier;

class Property extends \PhpParser\Builder\Property
{
    /**
     * 定义名称
     *
     * @time 2021年06月02日
     * @param string $name
     * @return Property
     */
    public static function name(string $name)
    {
        return new self($name);
    }


    public function fetch()
    {
        return $this->getNode();
    }

    /**
     * 设置注释
     *
     * @time 2021年06月01日
     * @param string $docComment
     * @return Property
     */
    public function setDocComment($docComment)
    {
        return parent::setDocComment(new Doc($docComment)); // TODO: Change the autogenerated stub
    }

    /**
     * 定义属性访问
     *
     * @time 2021年06月02日
     * @param $variable
     * @param $identifier
     * @return PropertyFetch
     */
    public static function defineIdentifier($variable, $identifier): PropertyFetch
    {
        return new PropertyFetch(new \PhpParser\Node\Expr\Variable($variable), new Identifier($identifier));
    }
}