<?php
namespace JaguarJack\Generate\Build;


use JaguarJack\Generate\Define;
use PhpParser\Comment\Doc;
use PhpParser\Node\Expr\PropertyFetch;
use PhpParser\Node\Expr\StaticPropertyFetch;
use PhpParser\Node\Identifier;

class Property extends \PhpParser\Builder\Property
{
    /**
     * 定义名称
     *
     * @time 2021年06月02日
     * @param string $name
     * @return Property
     */
    public static function name(string $name)
    {
        return new self($name);
    }


    /**
     * fetch
     *
     * @time 2021年06月08日
     * @return \PhpParser\Node|\PhpParser\Node\Stmt\Property
     */
    public function fetch()
    {
        return $this->getNode();
    }

    /**
     * set default
     *
     * @time 2021年06月08日
     * @param mixed $value
     * @return \JaguarJack\Generate\Build\Property
     */
    public function setDefault($value): Property
    {
        return parent::setDefault(Define::value($value)); // TODO: Change the autogenerated stub
    }

    /**
     * 设置注释
     *
     * @time 2021年06月01日
     * @param string $docComment
     * @return Property
     */
    public function setDocComment($docComment): Property
    {
        return parent::setDocComment(new Doc($docComment)); // TODO: Change the autogenerated stub
    }

    /**
     * 定义属性访问
     *
     * @time 2021年06月02日
     * @param $variable
     * @param $identifier
     * @return PropertyFetch
     */
    public static function defineIdentifier($variable, $identifier): PropertyFetch
    {
        return new PropertyFetch(new \PhpParser\Node\Expr\Variable($variable), new Identifier($identifier));
    }

    /**
     * 定义属性访问
     *
     * @time 2021年06月02日
     * @param $variable
     * @param $identifier
     * @return StaticPropertyFetch
     */
    public static function staticDefineIdentifier($variable, $identifier)
    {
        return new StaticPropertyFetch(new \PhpParser\Node\Expr\Variable($variable), new Identifier($identifier));
    }
}